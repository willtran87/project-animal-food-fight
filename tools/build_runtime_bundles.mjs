import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { runtimeBundleFiles, runtimeEntryGroups } from "./runtime-entry-groups.mjs";

const toolsDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(toolsDir, "..");
const distDir = path.join(repoRoot, "dist");
const appScriptsPath = path.join(repoRoot, "src", "app-scripts.js");
const checkOnly = process.argv.includes("--check");

function hashContent(content) {
  return `h-${crypto.createHash("sha256").update(content).digest("hex").slice(0, 12)}`;
}

function bundleContent(entry, names) {
  const sections = names.map((name) => {
    const relativePath = `src/${name}.js`;
    const source = fs.readFileSync(path.join(repoRoot, relativePath), "utf8").trimEnd();
    return `/* ${relativePath} */\n${source}`;
  });
  return `/* Generated by npm run build:runtime. Do not edit. Entry: ${entry}. */\n${sections.join("\n\n")}\n`;
}

function replaceBundleVersion(source, entry, fileName, version) {
  const pattern = new RegExp(`(${entry}:\\s*\\[\\s*"dist/${fileName.replace(".", "\\.")}"\\s*,\\s*")[^"]+("\\s*\\])`);
  if (!pattern.test(source)) throw new Error(`Missing ${entry} bundle entry in src/app-scripts.js`);
  return source.replace(pattern, `$1${version}$2`);
}

const bundles = Object.entries(runtimeEntryGroups).map(([entry, names]) => {
  const fileName = runtimeBundleFiles[entry];
  const content = bundleContent(entry, names);
  return { entry, fileName, content, version: hashContent(content) };
});

let appScripts = fs.readFileSync(appScriptsPath, "utf8");
for (const bundle of bundles) {
  appScripts = replaceBundleVersion(appScripts, bundle.entry, bundle.fileName, bundle.version);
}

const stale = [];
for (const bundle of bundles) {
  const outputPath = path.join(distDir, bundle.fileName);
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : null;
  if (current !== bundle.content) stale.push(path.relative(repoRoot, outputPath));
}
if (fs.readFileSync(appScriptsPath, "utf8") !== appScripts) stale.push(path.relative(repoRoot, appScriptsPath));

if (checkOnly) {
  if (stale.length) {
    console.error("Runtime bundles are stale:");
    stale.forEach((file) => console.error(`- ${file}`));
    process.exit(1);
  }
  console.log(`Runtime bundles are current (${bundles.map((bundle) => `${bundle.entry}:${bundle.version}`).join(", ")}).`);
  process.exit(0);
}

fs.mkdirSync(distDir, { recursive: true });
for (const bundle of bundles) fs.writeFileSync(path.join(distDir, bundle.fileName), bundle.content);
fs.writeFileSync(appScriptsPath, appScripts);
console.log(`Built ${bundles.length} runtime bundles.`);
